#include "Setting.h"

namespace mtv {

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  Setting::Setting(const QString name) {
    this->init();
    this->name = name;

  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  Setting::Setting(const QString name, bool value) {
    this->init();
    this->name = name;
    this->bValue = value;
    this->type = BOOLEAN;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  Setting::Setting(const QString name, const QString value) {
    this->init();
    this->name = name;
    this->sValue = value;
    this->type = STRING;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  Setting::Setting(const QString name, int value) {
    this->init();
    this->name = name;
    this->iValue = value;
    this->type = INTEGER;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  Setting::Setting(const QString name, double value) {
    this->init();
    this->name = name;
    this->dValue = value;
    this->type = DOUBLE;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  Setting::Setting(const QString name, Module *module, const QString frameName) {
    this->init();
    this->name = name;
    this->mValue = module;
    this->type = FRAME;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  void Setting::init() {
    this->readOnly = false;
    this->hasMaxValue = false;
    this->hasMinValue = false;
    this->maxValue = 0;
    this->minValue = 0;
    this->mValue = 0x0;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  bool Setting::isReadOnly() {
    return this->readOnly;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  void Setting::setReadOnly(bool value) {
    this->readOnly = value;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  bool Setting::hasMin() {
    return this->hasMaxValue;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  void Setting::setHasMin(bool value) {
    this->hasMinValue = value;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  int Setting::getMin() {
    return this->minValue;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  void Setting::setMin(int value) {
    this->minValue = value;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  bool Setting::hasMax() {
    return this->hasMaxValue;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  void Setting::setHasMax(bool value) {
    this->hasMaxValue = value;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  int Setting::getMax() {
    return this->maxValue;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  void Setting::setMax(int value) {
    this->maxValue = value;
  }


  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  void Setting::set(const QString value) {
    emit beforeSettingChanged(this);
    this->sValue = value;
    this->type = STRING;
    emit afterSettingChanged(this);
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  void Setting::set(bool value) {
    emit beforeSettingChanged(this);
    this->bValue = value;
    this->type = BOOLEAN;
    emit afterSettingChanged(this);
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  void Setting::set(int value) {
    emit beforeSettingChanged(this);
    this->iValue = value;
    this->type = INTEGER;
    emit afterSettingChanged(this);
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  void Setting::set(double value) {
    emit beforeSettingChanged(this);
    this->dValue = value;
    this->type = DOUBLE;
    emit afterSettingChanged(this);
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  void Setting::set(Module*module, const QString frameName) {
    emit beforeSettingChanged(this);
    this->mValue = module;
    this->frameName = frameName;
    this->type = FRAME;
    emit afterSettingChanged(this);
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  bool Setting::asBool() {
    Q_ASSERT(this->type == BOOLEAN);
    return this->bValue;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  QString Setting::asString() {
    Q_ASSERT(this->type == STRING);
    return this->sValue;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  double Setting::asDouble() {
    Q_ASSERT(this->type == DOUBLE);
    return this->dValue;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  int Setting::asInteger() {
    Q_ASSERT(this->type == INTEGER);
    return this->iValue;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  Module * Setting::getModule() {
    return this->mValue;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  QString Setting::getFrameName() const {
    return this->frameName;
  }

  /* -------------------------------------------------------------------------------------------
   *
   * ------------------------------------------------------------------------------------------- */
  QString Setting::getName() const {
    return this->name;
  }



}
